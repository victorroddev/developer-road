//typeof is an operand
//To know the type of an object in JavaScript


//Examples of use
var age = 3;
var an = ["Hallo","Willkomen"];
var aFunction = new Function(1 + 1);




console.log(typeof age); //Returns: number | (the type data of the variable age)
console.log(typeof(an)); //Returns: object | we can use type of operand with or with out parentheses.
console.log(typeof aFunction); //Returns function


